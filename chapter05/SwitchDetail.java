public class SwitchDetail{
	public static void main(String[] args) {
		 char c = 'a';
		 // char c = 'b';
		 // char c = 'e';
		 switch(c) {
		 	case 'a' :
		 		System.out.println("ok1");
		 		break;
		 	case 'b' :
		 		System.out.println("ok2");
		 		break;
		 	default :
		 		System.out.println("ok3");	
		 } 
	}
}
